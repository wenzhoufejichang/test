package com.hzm.domain;

import java.util.Date;

public class Customer {

	private String id;
	private Date times;
	
	private String idssss;
	
	public Date getTime() {
		return times;
	}

	public void setTime(Date time) {
		this.times = time;
	}

	public void setIdssss(String idssss) {
		this.idssss = idssss;
	}

	public String getIdssss() {
		return idssss;
	}

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Date getTimes() {
		return times;
	}

	public void setTimes(Date times) {
		this.times = times;
	}

}
